#include<iostream>
using namespace std;
int main(){
    int x = 5;
    int y = 2;
    cout<<x+y<<endl;
    cout<<x-y<<endl;
    cout<<x*y<<endl;
    cout<<x/y<<endl;

    //increment decrement operator

    int s = 7;
    cout<<s++<<endl;//x++>x=x+1
    cout<<s--<<endl;//x-->x=x-1

    // ++x and x++ have same outputs but different meanings one  is pre increment and one is post increment
    return 0;

}